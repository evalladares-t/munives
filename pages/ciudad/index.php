											<h5 class="pt-3 pl-4">Aspectos Socio Ambientales</h5>
											<p style="text-align: justify;">La interrelación entre la población y su ambiente tiene mayor perceptibilidad en la zona de playas (temporada de verano) y en los parques de cada grupo residencial, contándose las alamedas y los parques administrados por la Municipalidad Metropolitana de Lima. </p>
											<h5 class="pt-3 pl-4">Climatología</h5>
											<p style="text-align: justify;">El clima está caracterizado por ser subtropical, es árido, semi cálido y nuboso en distintas épocas del año, con una temperatura media anual que fluctúa entre los 15 ºC y 23ºC, los valores extremos corresponden a los meses de julio y febrero respectivamente, con una nubosidad media de 8 octavos, la humedad relativa media varía entre 80 y 100%, los vientos soplan durante el día, de norte a suroeste y durante la noche de suroeste a norte. La precipitación media es de 25 mm anuales. Los vientos tienen una velocidad media de 2 a 4 m/s esta baja intensidad en la velocidad de los vientos hace que los contaminantes de la atmósfera no se dispersen en la magnitud como debería ser, lo cual constituye un lento transporte de los mismos.</p>
											<h5 class="pt-3 pl-4">Agua y Alcantarillado</h5>
											<p style="text-align: justify;">Los servicios de agua potable y alcantarillado son administrados por SEDAPAL y la principal fuente de abastecimiento de agua potable es el río Rímac, cuyos caudales varían entre 15,2 m3/s (setiembre) y 66,2 m3/s (febrero, época de precipitaciones en la sierra).</p>
											<p style="text-align: justify;">La precipitación pluvial es mínima (25,48 mm/año), por lo que no podría ser considerada como fuente de recursos hídricos. Las aguas subterráneas son otra fuente importante de agua potable; SEDAPAL registra en el distrito la existencia de 15 pozos (para riego y bebida de animales) que generan 537 L/s de agua.</p>
											<h5 class="pt-3 pl-4">Población</h5>
											<p style="text-align: justify;">Una de las particularidades de la dinámica poblacional en el distrito de Villa el Salvador es la de haber sido receptora natural de un gran número de personas demandando un espacio para poder habitar. Este fenómeno ha devenido en un acelerado crecimiento de su población a consecuencia de un constante proceso migratorio o de reubicación de pobladores.</p>
											<p style="text-align: justify;">Una de las características de Villa el Salvador desde su fundación fue la de haber sido una de las pocas ciudades populares construidas de manera planificada.</p>
											<p style="text-align: justify;">En un inicio se pensó que esta joven ciudad organizaría su territorio en 10 sectores y que albergaría un determinado número de viviendas. Sin embargo, el crecimiento poblacional que experimentaría esta ciudad en las siguientes décadas, desbordarían las proyecciones más conservadoras.</p>											
