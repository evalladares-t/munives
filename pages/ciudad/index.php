                                            <h4>Datos Generales</h4>
											<p style="text-align: justify;">El distrito se encuentra ubicado al sur de Lima Metropolitana y es uno de los 43 distritos de la provincia de Lima, departamento de Lima. Cuenta con una superficie de 35,46 km2 y tiene una altitud media de 143 msnm. Se fundó el 11 de mayo de 1971, adquiriendo la categoría de distrito el 1 de junio de 1983 a través de la Ley N° 23695. En la ilustración 1 se muestra la ubicación del distrito.</p>
											<h5 class="pt-3 pl-4">Ubicación y Geografía</h5>
											<p style="text-align: justify;">El distrito de Villa el Salvador está ubicado al sur de Lima, entre los Km 15.5 y 25 de la Carretera Panamericana Sur. Ocupa un área de 35.460 km2, subdividido en territorios, urbanizaciones, sectores, grupos y manzanas.
												Está asentado sobre el desierto de la Tablada de Lurín, en la zona de intercuenca de Lurín y Rímac.</p>
											<p>Está comprendido entre las coordenadas geográficas:</p>
