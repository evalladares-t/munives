											<p style="text-align: justify;">Tengan la plena confianza y tranquilidad que cada sol que pagan en sus tributos seguirá siendo bien invertido en las obras de infraestructura vial y peatonal que han quedado pendientes, además de mejorar los servicios que se están brindando a toda la comunidad.</p>
											<p style="text-align: justify;">Hace 7 años comenzó la transformación de Villa El Salvador construyéndose 2 millones y medio de m2 de pistas y veredas de concreto que le dan una mayor visión de urbanismo a los grupos residenciales, avenidas troncales y vías metropolitanas que estuvieron olvidadas por muchos años. Ahora vamos a complementar el cambio con más áreas verdes y alamedas que mejoren el ornato.</p>
