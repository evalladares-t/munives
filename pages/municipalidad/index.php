											<p style="text-align: justify;">Vamos a mejorar y repotenciar los servicios a los ciudadanos adquiriendo una nueva flota de camiones recolectores para reforzar el servicio de recojo de basura, camionetas para hacer un mejor patrullaje de serenazgo y maquinaria que servirá para abaratar los costos de las obras. Por ello, exhorto a mis vecinos que aún no se ponen al día en sus pagos, se acojan a los diferentes beneficios tributarios que la municipalidad pone a su alcance.</p>
											<p style="text-align: justify;">Este año seremos sede de los Juegos Panamericanos Lima 2019, recibiendo a cerca de 10 mil deportistas de las diferentes delegaciones participantes, por ello tenemos la tarea de hacerlos sentir como en su casa y dar un buena imagen ante el mundo.</p>
											<p style="text-align: justify;">Vecinos y autoridades trabajaremos de la mano para que nuestro Villa El Salvador siga creciendo y modernizándose rumbo a sus 50 años de fundación.</p>		
