										<p style="text-align: justify;">En el plano interno, la Municipalidad de Villa El Salvador se rige por el Reglamento de Organización y Funciones aprobado mediante Ordenanza Municipal N° 369 – MVES – 2017, el Reglamento Interno de Concejo, RIC, aprobado mediante Edicto N° 001 del año 1999, modificado mediante Edicto N° 002 del mismo año y vuelto a modificar mediante Ordenanza Municipal 140; y el Texto Único de Procedimientos Administrativos, aprobado mediante Ordenanza Municipal N° 310- MVES/2014.</p>
