										<h4>Visión - Misión</h4>
										<h5 class="pt-3">Visión:</h5>
										<p style="text-align: justify;">Ser una Municipalidad líder, con una gestión eficiente, transparente y participativa, donde sin distinción económica, social o de género se sumen esfuerzos para posicionar a Villa El Salvador como una ciudad ordenada, segura, moderna, inclusiva y saludable. </p>
										<h5 class="pt-3">Misión:</h5>
										<p style="text-align: justify;">Representar al vecino consolidando su participación democrática, con servicios de calidad, promoviendo el desarrollo económico, social y ambiental mediante un manejo responsable y transparente de los recursos públicos.</p>
