          <p align="justify" class="brief"><strong>¿Qué es el Programa de Promoción de la Mujer e igualdad de oportunidades?</strong></p>
          <p align="justify" class="brief">El Programa de Promoción de la Mujer e Igualdad de Oportunidades es el órgano encargado de las actividades relacionadas con el desarrollo y promoción de los derechos de la mujer, así como de promover el enfoque de igualdad de oportunidades entre mujeres y varones. Fuente: R.A. 170-2008- ROF.          </p>
          <p align="justify" class="brief"><strong>¿Cuáles son sus funciones del Programa de la Mujer?          </strong></p>
          <p align="justify" class="brief">Son funciones del Programa de Promoción de la Mujer:          </p>
          <p align="justify" class="brief">Formular y promover la ejecución, actualización, monitoreo y evaluación del Plan de igualdad de oportunidades entre varones y mujeres del distrito.
            
            
            
            Formular, proponer, ejecutar, monitorear y evaluar políticas, planes, programa y proyectos, orientados a promover el desarrollo integral y sostenible de las mujeres, para procurar igualdad de oportunidades entre mujeres y varones. 
            
            
            
            Promover los derechos sociales, económicos, políticos y culturales de las mujeres, niñas, adolescentes y mujeres adultas, en todas las instancias e instituciones y espacios sociales.
            
            
            
            Proponer estrategias de transversalización del enfoque de género en los diferentes niveles de la institución municipal.
            
            
            
            Conducir la implementación del enfoque de género en la Municipalidad de Villa El Salvador, instituciones públicas y privadas.          </p>
          <p align="justify" class="brief">Promover el acceso y hacer vigilancia de la calidad y calidez de los servicios brindados a las mujeres.
            
            
            
            Promover e institucionalizar el espacio de encuentro, concertación y articulación con las organizaciones de mujeres y redes que promueven la equidad de género y defensa de las mujeres.          </p>
          <p align="justify" class="brief">Promover y participar en la ejecución de estudios sobre los derechos, demandas, estado y necesidades de las mujeres del distrito de Villa El Salvador, identificando los diferentes grupos de acuerdo a su realidad e intereses.
            
            
            
            Implementar un centro de servicios de asesoría, información y orientación que promueva el derecho de las mujeres.          </p>
          <p align="justify" class="brief">Promover y fortalecer iniciativas orientadas a revertir situaciones de discriminación y violencia contra las mujeres.
            
            
            
          Promover el liderazgo y participación de las mujeres en las actividades políticas, económicas y sociales que se desarrollan en el distrito de Villa El Salvador.</p>
          <p align="justify" class="brief">Establecer canales de concertación con y entre instituciones que actúan en defensa de las niñas, las adolescentes y las mujeres adultas mayores para potenciar los recursos disponibles. 
            
            
            
            Las demás que le sean delegadas por la autoridad superior.            </p>
