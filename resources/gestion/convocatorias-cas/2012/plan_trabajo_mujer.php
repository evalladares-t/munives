          <p class="brief" align="justify"> Implementación de la Oficina del Programa de Promoción de la Mujer e Igualdad de Oportunidades en la Municipalidad de Villa El Salvador. 
            
            Aprobación de 3 Ordenanzas Municipales a favor de la igualdad de oportunidades.
            
            Concluir el Plan de Igualdad de Oportunidades, reuniones, validación, presentación. Coordinaciones con el Equipo Impulsor de Políticas Inclusivas con equidad de género.
            
            Proyecto: “Hacia un Gobierno Local y Presupuesto Participativo con enfoque de genero”.
            
            
            
            2. Sensibilizar a hombres y mujeres en el conocimiento y ejercicio de los derechos de la mujer (salud, educación, empleo y participación).
            
            
            
